# +  adds numbers
# -  subtracts numbers
# *  multiplies numbers
# /  divides numbers
# // divides and removes the decimal part
# %  gives the remainder
# ** raises a number to a power

# Mathematical operations in Python

print(10 + 5)   # addition
print(10 - 5)   # subtraction
print(10 * 5)   # multiplication
print(10 / 5)   # division
print(10 // 3)  # floor division - result without decimal part
print(10 % 3)   # modulo - remainder after division
print(2 ** 3)   # exponent - 2 to the power of 3
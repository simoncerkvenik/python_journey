# =========================
# STUDENT SCORES
# =========================

# List of student scores
student_scores = [88, 92, 75, 64, 97, 83, 71, 90]

# Start with the first score as the current highest score
highest_score = student_scores[0]

# Loop through every score in the list
for score in student_scores:

    # If current score is bigger than highest_score
    if score > highest_score:

        # Save current score as the new highest_score
        highest_score = score

# Print the winner score
print(f"Zmagovalec je zmagal z {highest_score} točkami")


